public class J5IntToOtherNum {
    public static void main(String args[]){
        int x = 64;
        System.out.println("Integer to Binary: " + Integer.toBinaryString(x));
        System.out.println("Integer to Hexadecimal: " + Integer.toHexString(x));
        System.out.println("Integer to Octal: " + Integer.toOctalString(x));
        System.out.println("Integer to Octal: " + Integer.toString(x,2));
    }
}
